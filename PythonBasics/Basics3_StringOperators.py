# String Special Operators

a  = 'Hello'
b = 'Python'

print("a+b: ", a+b)         # Output: HelloPython
print()
print("a*b: ", a*2)         # Output: HelloHello
print()
print("a[1]: ", a[1])       # Output: e
print()
print("a[1:4]: ", a[1:4])   # Output: ell
print()
print("'Heo' in a: ", 'Heo' in a)   # Output: False
print()
print("'He' in a: ", 'He' in a)   # Output: True
print()
print ("Python is %s based and latest version is %d !" % ('OOPs', 3))  # Output: Python is OOPs based and latest version is 3 !
print()





